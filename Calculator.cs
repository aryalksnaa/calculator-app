class Calculator
{
	static void Main(string[]args)
	{
		int a = 10;
		int b = 6;
		
		Console.WirteLine("Hasil penambahan: {0} + {1} + {2}", a, b, penambahan(a,b);
		Console.WirteLine("Hasil pengurangan: {0} + {1} + {2}", a, b, pengurangan(a,b);
		
		Console.WirteLine("\nTekan sembarang key untuk keluar");
		Console.Readkey();
	}
	
	static int Penambahan (int a, int b)
	{
		return a + b;
	}
	
	static int pengurangan (int a, int b)
	{
		return a - b;
	}
}